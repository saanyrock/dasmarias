        <h3>Semi Joias e Bijuterias</h3><br />
        <h2>Para evitar riscos guarde-as em compartimentos individuais ou em saquinhos de plastico ou veludo.<br><br />
Ao usar cremes hidratantes, perfumes, pomadas e quaisquer cosméticos aguarde a absorção completa do produto antes de colar sua semi-joia ou bijuteria.<br><br />
Produtos de higiene e limpeza, e produtos químicos em geral podem causar danos as peças.<br><br />
Peças com partes coladas podem descolar ao entrar em contato com água morna ou outras fontes de calor. <br><br />
Apos o uso, limpe as peças com uma flanela macia, friccionado levemente para dar brilho.<br><br />
Perolas, corais e turquesas devem ser limpas somente com flanela seca.<br><br />
Peças de metal e pedras naturais podem ser lavadas com água e sabão neutro, utilize uma escova macia para uma limpeza mais profunda. Seque bem e faça um polimento com flanela.<br><br />
Peças banhadas com metal dourado ou preteadas não devem ser limpas com as chamadas “FLANELAS MAGICAS”, elas possuem polidor químico que retira o banho das peças. Apenas peças de ouro e prata maciça podem ser limpas com essas flanelas.<br><br />
Nunca use acetona, álcool, creme dental e solventes na limpeza de semi-joia e bijuteria. Esses produtos danificam as peças.<br><br />
As pedras naturais podem ter a sua cor e resistência alteradas quando expostas ao calor extremo.<br><br />
